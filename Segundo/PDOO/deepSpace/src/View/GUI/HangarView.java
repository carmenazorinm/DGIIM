    public void setHangar(HangarToUI h){
        if(h==null){
            setVisible(false);
        }else{
            jPanel1.removeAll();
            maxel.setText(Integer.toString(h.getMaxElements()));
            
            ArrayList<WeaponToUI> weap = h.getWeapons();
            ArrayList<ShieldToUI> sb = h.getShieldBoosters();
            
            for(int i = 0; i<weap.size(); i++ ){
                WeaponsView wview = new WeaponsView();
                wview.setWeapon(weap.get(i));
                jPanel1.add(wview);
            }
            
            for(int i = 0; i<sb.size(); i++ ){
                ShieldsView sview = new ShieldsView();
                sview.setShieldBooster(sb.get(i));
                jPanel1.add(sview);
            }
        }
        repaint();
        revalidate();
    }

